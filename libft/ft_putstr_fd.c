#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int pos;

	pos = 0;
	if (!s || fd < 0)
		return ;
	while (s[pos] != '\0')
	{
		ft_putchar_fd(s[pos], fd);
		pos++;
	}
}
